# number multiple of 5

# a number multiple of 5 means, it should be divisible of 5 ...it means, it will give remainder to be zero.


number = int(input("Enter a number :"))
if number % 5 == 0:
    print("The number is a multiple of 5")
else:
    print("The number is not a multiple of 5")
    
